package DAY7;

public class Card {
    String symbol;
    int power;
    int powtorzenia;

    public int getPower() {
        return power;
    }

    public int getPowtorzenia() {
        return powtorzenia;
    }

    public String getSymbol() {
        return symbol;
    }

    public void setPower(int power) {
        this.power = power;
    }

    public void setPowtorzenia(int powtorzenia) {
        this.powtorzenia = powtorzenia;
    }

    public void setSymbol(String symbol) {
        this.symbol = symbol;
    }
}
